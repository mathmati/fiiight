// Test driver page logic: mounts the test zip with the real fs-shim, runs
// fstest.wasm, and flags completion for the CDP runner (build/wasm/test/run.mjs).
// ?mode= selects the mount path: "eager" (default, mountZip), "lazy"
// (mountZipIndex against the Range-capable dev server, with JS-side
// pre-open assertions), "lazy-norange" (mountZipIndex against a URL the
// dev server refuses Range on -- must fall back to a full download).
"use strict";
window.__ikemenBootLog = [];
window.__testDone = false;

(async () => {
	const log = (l) => window.__ikemenBootLog.push(l);
	const check = (name, ok, detail) => log(ok ? "PASS: " + name : "FAIL: " + name + " -- " + detail);
	const fsCall = (name, ...args) => new Promise((resolve, reject) => {
		globalThis.fs[name](...args, (err, res) => err ? reject(err) : resolve(res));
	});
	async function readAll(path) {
		const fd = await fsCall("open", path, 0, 0);
		try {
			const st = await fsCall("fstat", fd);
			const buf = new Uint8Array(st.size);
			let off = 0;
			while (off < st.size) {
				const n = await fsCall("read", fd, buf, off, st.size - off, off);
				if (n <= 0) break;
				off += n;
			}
			return new TextDecoder().decode(buf.subarray(0, off));
		} finally {
			await fsCall("close", fd);
		}
	}
	// Must match the zip generated by run.mjs.
	const STORED_TXT = "hello wasm\n";
	const DEFLATE_TXT = "nested content: deflate me deflate me deflate me\n";

	try {
		const mode = new URLSearchParams(location.search).get("mode") || "eager";
		if (mode === "eager") {
			await mountZip("/test/content.zip", "/ikemen", () => {});
		} else if (mode === "lazy-norange") {
			const res = await mountZipIndex("/test/content.zip?norange=1", "/ikemen", {});
			check("fallback-eager", res.lazy === false && res.files > 0, JSON.stringify(res));
		} else {
			const res = await mountZipIndex("/test/content.zip", "/ikemen", {});
			check("lazy-mounted", res.lazy === true && res.files > 0, JSON.stringify(res));
			// stat/fstat-free metadata must be right BEFORE any open.
			const before = globalThis.__fsLazyStats.hydrated;
			const s1 = await fsCall("stat", "/ikemen/data/hello.txt");
			check("lazy-stat-stored", s1.size === STORED_TXT.length, "size=" + s1.size);
			const s2 = await fsCall("stat", "/ikemen/data/sub/nested.txt");
			check("lazy-stat-deflate", s2.size === DEFLATE_TXT.length, "size=" + s2.size);
			const names = await fsCall("readdir", "/ikemen/data");
			check("lazy-readdir", names.includes("hello.txt") && names.includes("sub") && names.includes("empty.bin"), JSON.stringify(names));
			check("lazy-stat-no-hydration", globalThis.__fsLazyStats.hydrated === before,
				"stat/readdir hydrated " + (globalThis.__fsLazyStats.hydrated - before) + " files");
			// Contents after open: one stored and one deflated entry.
			check("lazy-read-stored", await readAll("/ikemen/data/hello.txt") === STORED_TXT, "content mismatch");
			check("lazy-read-deflate", await readAll("/ikemen/data/sub/nested.txt") === DEFLATE_TXT, "content mismatch");
			check("lazy-hydrated-on-open", globalThis.__fsLazyStats.hydrated >= before + 2,
				"hydrated=" + globalThis.__fsLazyStats.hydrated);
		}
		globalThis.process.chdir("/ikemen");
		const go = new Go();
		const { instance } = await WebAssembly.instantiateStreaming(
			fetch("/test/fstest.wasm"), go.importObject);
		await go.run(instance);
	} catch (err) {
		window.__ikemenBootLog.push("HARNESS-ERROR: " + ((err && err.stack) || err));
	} finally {
		window.__testDone = true;
		document.getElementById("out").textContent = window.__ikemenBootLog.join("\n");
	}
})();
